-- Tabla: ROLES
CREATE TABLE ROLES (
    ID_ROL NUMBER(19, 0) GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE_ROL VARCHAR2(50) NOT NULL UNIQUE,
    CONSTRAINT PK_ROLES PRIMARY KEY (ID_ROL)
);

-- Tabla: INTENDENCIAS
CREATE TABLE INTENDENCIAS (
    COD_IRE VARCHAR2(50) NOT NULL,
    NOMBRE_IRE VARCHAR2(100) NOT NULL,
    CONSTRAINT PK_INTENDENCIAS PRIMARY KEY (COD_IRE)
);

-- Tabla: USUARIOS
CREATE TABLE USUARIOS (
    ID_USUARIO NUMBER(19, 0) GENERATED BY DEFAULT AS IDENTITY,
    USERNAME VARCHAR2(50) NOT NULL UNIQUE,
    PASSWORD VARCHAR2(255) NOT NULL,
    EMAIL VARCHAR2(255),
    ID_ROL NUMBER(19, 0),
    COD_IRE VARCHAR2(20),
    ESPECIALISTA_NUM NUMBER(10, 0),
    DEBE_CAMBIAR_PASSWORD NUMBER(1, 0) DEFAULT 1,
    ACTIVO NUMBER(1, 0) DEFAULT 1,
    CONSTRAINT PK_USUARIOS PRIMARY KEY (ID_USUARIO),
    CONSTRAINT FK_USUARIOS_ROLES FOREIGN KEY (ID_ROL) REFERENCES ROLES(ID_ROL)
);

-- Tabla: INCIDENCIAS
CREATE TABLE INCIDENCIAS (
    ID_INCIDENCIA NUMBER(19, 0) GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE_INCIDENCIA VARCHAR2(255) NOT NULL,
    ESPECIALISTA_NUM NUMBER(10, 0),
    CONSTRAINT PK_INCIDENCIAS PRIMARY KEY (ID_INCIDENCIA)
);

-- Tabla: TICKETS
CREATE TABLE TICKETS (
    ID_TICKET VARCHAR2(100) NOT NULL,
    COD_IRE VARCHAR2(20),
    ASUNTO CLOB,
    FECHA_CREACION TIMESTAMP,
    ESTADO VARCHAR2(100) DEFAULT 'PENDIENTE',
    DRIVEFOLDERID VARCHAR2(255),
    LINK_DRIVE VARCHAR2(500),
    ID_INCIDENCIA NUMBER(19, 0),
    ESPECIALISTA_NUM NUMBER(10, 0),
    ID_USUARIO_CREADOR NUMBER(19, 0),
    FECHA_RESOLUCION TIMESTAMP,
    TOKEN_CONFIRMACION VARCHAR2(120),
    FECHA_EXPIRACION_TOKEN TIMESTAMP,
    MOTIVO_RECHAZO VARCHAR2(500),
    CONSTRAINT PK_TICKETS PRIMARY KEY (ID_TICKET),
    CONSTRAINT FK_TICKETS_INCIDENCIA FOREIGN KEY (ID_INCIDENCIA) REFERENCES INCIDENCIAS(ID_INCIDENCIA),
    CONSTRAINT FK_TICKETS_CREADOR FOREIGN KEY (ID_USUARIO_CREADOR) REFERENCES USUARIOS(ID_USUARIO)
);

-- Tabla: TICKET_LOGS
CREATE TABLE TICKET_LOGS (
    ID_LOG NUMBER(19, 0) GENERATED BY DEFAULT AS IDENTITY,
    ID_TICKET VARCHAR2(100),
    ESTADO_ANTERIOR VARCHAR2(100),
    ESTADO_NUEVO VARCHAR2(100),
    USUARIO VARCHAR2(100),
    FECHA TIMESTAMP,
    DETALLES CLOB,
    CONSTRAINT PK_TICKET_LOGS PRIMARY KEY (ID_LOG)
);

-- Tabla: TICKET_SEQUENCES
CREATE TABLE TICKET_SEQUENCES (
    COD_IRE VARCHAR2(50) NOT NULL,
    CURRENT_VALUE NUMBER(19, 0),
    CONSTRAINT PK_TICKET_SEQUENCES PRIMARY KEY (COD_IRE)
);
